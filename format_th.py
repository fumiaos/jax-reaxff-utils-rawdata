def convert_text(input_text):
    # 将输入文本按行分割
    lines = input_text.strip().split("\n")

    # 初始化结果字符串
    result = []

    # 遍历每一行，按要求格式化
    for line in lines:
        # 添加双引号和转义字符
        formatted_line = f'{line}\n'
        #formatted_line = f'"{line}"\n'
        result.append(formatted_line)

    # 将结果列表拼接成字符串
    output_text = "".join(result)

    return output_text

